no campo da ciência da computação.
      <br><br>
       Analista de Sistemas Manoel Neto, <b>Bacharel em Sistemas de Informação,</b>
      <b>Universidade Estácio de Sá da Bahia.</b> Cidade natal Capim Grosso-Ba. Sua educação desde infância 
foi na Cidade de Nova Fátima-Ba 230 Km da Capital baiana, onde concluiu o Ensino Médio e realizou alguns trabalho na sua cidade. 
Conhece bastante a região da bacia do jacuípe e esta disponível para oportunidade de trabalho em qualquer região do Brasil. Ingressou na faculdade em 2014 na área da Ciência da Computação.
 "Pra mim foi sempre um dos vários sonhos, ingressar em uma faculdade e estudar sobre a ciência da computação, desde infância gostava de computadores e trabalhar com 
eles, gosto de ciência e a computação foi sempre um desafio para mim e entendia que a computação traz um grande avanço para humanidade,
na questão astronômica e outros meios para nossas vidas aqui na terra. Sou proativo gosto de Ciência e do Evangelho. "Ao mergulhar na beleza da
Criação Divina do nosso planeta estamos instigando harmonias matemáticas na mente do Criador". Marcelo Gleiser.
E tenho alguns hobby, um dos é tocar violão entre outros instrumentos. Gosto do que faço e também busco sempre a qualidade 
e inovação nos meus trabalhos para pequenas e grandes empresas seja qual for a atividade, ramo ou segmento. Nós seres humanos samos curiosos e gostamos
de entender e aprender as coisas, então andar sempre atualizado 
é importante" Manoel Neto.  Objetivo específicos análisar e implementar 
